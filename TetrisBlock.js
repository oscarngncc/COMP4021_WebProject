function getColumnLength(Option)
{
    var LongestLength = 0;
    for ( var i = 0; i < 4; i++ )
    {
        var ColLength = 0;
        for ( var j = 0; j <4; j++ )
        {
            if ( Option[i][j] == 1 ){ ColLength = j; }
        }
        if ( ColLength >= LongestLength ){ LongestLength = ColLength; }
    }
    return LongestLength + 1;
}


function getRowLength(Option)
{
    var LongestLength = 0;
    for ( var i = 0; i < 4; i++ )
    {
        var RowLength = 0;
        for ( var j = 0; j <4; j++ )
        {
            if ( Option[i][j] == 1 ){ RowLength = i; }
        }
        if ( RowLength >= LongestLength ){ LongestLength = RowLength; }
    }
    return LongestLength + 1;
}

function getRandomValue()
{
    return Math.floor( Math.random() * TetrisBlocksOption.length );
}

function getOptionList(value)
{
    if (value == 999){ return SpOptionList; }
    return TetrisBlocksOption[value];
}

function getOption(value, Rotation=0)
{
    if (value == 999){ return SpOptionList[Rotation]; }
    return TetrisBlocksOption[value][Rotation];
}






